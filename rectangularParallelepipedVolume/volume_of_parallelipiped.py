# This program calculates the volume of a rectangular parallelepiped
# Volume = Length * width * height

length = int(input("Enter the length of the parallelepiped\n"))
width = int(input("Enter the width of the parallelepiped\n"))
height = int(input("Enter the height of the parallelepiped\n"))
volume = length * width * height
print("The volume of the parallelepiped is {}".format(volume))